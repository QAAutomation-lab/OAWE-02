package day11Operators;

public class Arithop {

	public static void main(String[] args) {
		// Variables Definition and Initialization
		int number1 = 13, number2 = 4;
		// Addition Operation
		int res = number1 + number2;
		System.out.println("Sum is: " + res);//17
		// Subtraction Operation
		res = number1 - number2;
		System.out.println("Difference is : " + res);//9
		// Multiplication Operation
		res = number1 * number2;
		System.out.println("Multiplied value is : " + res);//52
		// Division Operation
		res = number1 / number2;
		System.out.println("Quotient is : " + res);//3
		// Modulus Operation
		res = number1 % number2;
		System.out.println("Remainder is : " + res);//1
	}
}